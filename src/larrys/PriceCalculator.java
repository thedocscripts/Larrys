    private void jTextField1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyReleased
        // TODO add your handling code here:
         // TODO add your handling code here:
        VehList.removeAllItems();
        String veh = jTextField1.getText().toLowerCase().toString();
        System.out.println(veh);
        for (int i = 0; i < carro.size(); i++) {
            if(carro.get(i).toLowerCase().contains(veh)){
                cmitem ss = new cmitem(carro.get(i), precc.get(i));
                VehList.addItem(ss);
            }
              
            
        }
        
        
        
        if(VehList.getItemCount() == 1){
            Object item = VehList.getSelectedItem();
            
                String value = ((cmitem)item).getValue();
                VehPriceInput1.setText(value);
            
            
        }
    }//GEN-LAST:event_jTextField1KeyReleased

    private void TunePriceInput1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TunePriceInput1KeyReleased
        int vehprice = Integer.parseInt(VehPriceInput1.getText().toString());
        if(TunePriceInput1.getText() != null){
            int tuneprice = Integer.parseInt(TunePriceInput1.getText().toString());
            int tottax = vehprice + tuneprice;
            TaxPriceInput.setText(String.valueOf(tottax) + "$");
            BuyPriceInput.setText(String.valueOf(tottax * 0.50) + "$");
            SalePriceInput.setText(String.valueOf(tottax * 0.70) + "$");
        }
        
        
    }//GEN-LAST:event_TunePriceInput1KeyReleased

